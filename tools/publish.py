def _title_page(strategy: TextPlacementStrategy, img_path: str) -> str:
    """Generate title page Typst source."""
    if strategy == TextPlacementStrategy.text_always_below:
        return "\n".join([
            "#page[",
            f'  #box(width: 100%, height: 55%, clip: true)[#image("{img_path}", width: 100%, height: 100%, fit: "cover")]',
            "  #rect(width: 100%, height: 45%, fill: primary)[",
            "    #align(center + horizon)[",
            "      #pad(x: 2cm)[",
            '        #text(font: heading-font, size: 36pt, weight: "bold", fill: secondary)[#book-title]',
            "        #v(0.8em)",
            "        #text(size: 14pt, fill: secondary.darken(10%))[#book-author]",
            "      ]",
            "    ]",
            "  ]",
            "]",
        ])
    if strategy == TextPlacementStrategy.text_on_left:
        return "\n".join([
            "#page[",
            "  #rect(width: 100%, height: 100%, fill: primary)[",
            "    #align(center + horizon)[",
            "      #pad(x: 2cm)[",
            '        #text(font: heading-font, size: 30pt, weight: "bold", fill: secondary)[#book-title]',
            "        #v(1em)",
            "        #text(size: 14pt, fill: secondary.darken(10%))[#book-author]",
            "      ]",
            "    ]",
            "  ]",
            "]",
        ])
    # text_overlay_with_reserved_zone
    return "\n".join([
        "#page[",
        "  #rect(width: 100%, height: 100%, fill: primary)[",
        "    #align(center + horizon)[",
        "      #pad(x: 2cm)[",
        '        #text(font: heading-font, size: 36pt, weight: "bold", fill: secondary)[#book-title]',
        "        #v(1em)",
        "        #text(size: 14pt, fill: secondary.darken(10%))[#book-author]",
        "      ]",
        "    ]",
        "  ]",
        "]",
    ])


def _story_page(
    strategy: TextPlacementStrategy,
    img_path: str,
    text: str,
    page_num: int,
) -> str:
    """Generate a single story page's Typst source."""
    escaped = _escape_typst(text)
    if strategy == TextPlacementStrategy.text_always_below:
        return "\n".join([
            f"// Page {page_num}",
            "#page[",
            f'  #box(width: 100%, height: 60%, clip: true)[#image("{img_path}", width: 100%, height: 100%, fit: "cover")]',
            "  #rect(width: 100%, height: 40%, fill: secondary)[",
            "    #pad(x: 2cm, top: 1cm, bottom: 0.8cm)[",
            "      #align(center + horizon)[" + escaped + "]",
            "    ]",
            f"    #place(bottom + center, dy: -0.4cm)[#text(size: 9pt, fill: luma(150))[{page_num}]]",
            "  ]",
            "]",
        ])
    if strategy == TextPlacementStrategy.text_on_left:
        # Facing pages: text on verso, illustration on recto
        return "\n".join([
            f"// Spread — page {page_num}",
            "#page[",
            "  #rect(width: 100%, height: 100%, fill: secondary)[",
            "    #pad(left: 1.5cm, right: 2cm, top: 3cm, bottom: 1.5cm)[",
            "      #align(horizon)[" + escaped + "]",
            "    ]",
            f"    #place(bottom + left, dx: 1.5cm, dy: -0.5cm)[#text(size: 9pt, fill: luma(150))[{page_num}]]",
            "  ]",
            "]",
            "#page[",
            f'  #image("{img_path}", width: 100%, height: 100%, fit: "cover")',
            f"  #place(bottom + right, dx: -0.5cm, dy: -0.5cm)[#text(size: 9pt, fill: luma(150))[{page_num + 1}]]",
            "]",
        ])
    # text_overlay_with_reserved_zone — full-bleed + overlay
    return "\n".join([
        f"// Page {page_num}",
        "#page[",
        f'  #image("{img_path}", width: 100%, height: 100%, fit: "cover")',
        "  #place(top + left, dy: 60%,",
        "    rect(width: 100%, height: 40%, fill: white.transparentize(20%))[",
        "      #pad(x: 1.2cm, y: 0.8cm)[",
        "        #align(center)[" + escaped + "]",
        "      ]",
        "    ]",
        "  )",
        f"  #place(bottom + right, dx: -0.5cm, dy: -0.3cm)[#text(size: 9pt, fill: white)[{page_num}]]",
        "]",
    ])


def _closing_page(
    strategy: TextPlacementStrategy,
    img_path: str,
    text: str,
    page_num: int,
) -> str:
    """Generate the closing page Typst source."""
    escaped = _escape_typst(text)
    if strategy == TextPlacementStrategy.text_always_below:
        return "\n".join([
            f"// Closing — page {page_num}",
            "#page[",
            f'  #box(width: 100%, height: 55%, clip: true)[#image("{img_path}", width: 100%, height: 100%, fit: "cover")]',
            "  #rect(width: 100%, height: 45%, fill: secondary)[",
            "    #align(center + horizon)[",
            "      #pad(x: 2cm)[",
            '        #text(font: heading-font, size: 22pt, weight: "bold", fill: primary)[Tamat]',
            "        #v(0.6em)",
            "        " + escaped,
            "        #v(1.5em)",
            f"        #text(size: 9pt, fill: luma(150))[{page_num}]",
            "      ]",
            "    ]",
            "  ]",
            "]",
        ])
    if strategy == TextPlacementStrategy.text_on_left:
        return "\n".join([
            f"// Closing — page {page_num}",
            "#page[",
            "  #rect(width: 100%, height: 100%, fill: secondary)[",
            "    #align(center + horizon)[",
            "      #pad(x: 2cm)[",
            '        #text(font: heading-font, size: 22pt, weight: "bold", fill: primary)[Tamat]',
            "        #v(1em)",
            "        " + escaped,
            "        #v(2em)",
            f"        #text(size: 9pt, fill: luma(150))[{page_num}]",
            "      ]",
            "    ]",
            "  ]",
            "]",
        ])
    # full_bleed closing
    return "\n".join([
        f"// Closing — page {page_num}",
        "#page[",
        f'  #image("{img_path}", width: 100%, height: 100%, fit: "cover")',
        "  #place(top + left, dy: 35%,",
        "    rect(width: 100%, height: 30%, fill: white.transparentize(15%))[",
        "      #pad(x: 2cm, y: 0.8cm)[",
        "        #align(center + horizon)[",
        '          #text(font: heading-font, size: 20pt, weight: "bold", fill: primary)[Tamat]',
        "          #v(0.4em)",
        "          " + escaped,
        "        ]",
        "      ]",
        "    ]",
        "  )",
        f"  #place(bottom + right, dx: -0.5cm, dy: -0.3cm)[#text(size: 9pt, fill: luma(100))[{page_num}]]",
        "]",
    ])


def _generate_book_typst(
    *,
    images: list[Path],
    page_texts: list[str],
    scaffold: NarrativeScaffold,
    style_lock: StyleLock,
    title: str,
    author: str,
) -> str:
    """Generate complete Typst source for a children's book."""
    strategy = style_lock.text_placement_strategy
    page_w, page_h = _PAGE_DIMS[strategy]

    parts: list[str] = [
        _typst_header(
            palette=list(style_lock.palette),
            font=style_lock.typography,
            title=title,
            author=author,
            age_group=scaffold.target_age.value,
            page_width=page_w,
            page_height=page_h,
        ),
    ]

    # Title page (uses first image)
    parts.append(_title_page(strategy, str(images[0].resolve())))

    # Story pages (images 2..N-1)
    for idx in range(1, len(images) - 1):
        parts.append("")
        parts.append(
            _story_page(
                strategy,
                img_path=str(images[idx].resolve()),
                text=page_texts[idx],
                page_num=idx + 1,
            )
        )

    # Closing page (last image)
    parts.append("")
    last = len(images) - 1
    parts.append(
        _closing_page(
            strategy,
            img_path=str(images[last].resolve()),
            text=page_texts[last],
            page_num=last + 1,
        )
    )

    return "\n".join(parts)


# ---------------------------------------------------------------------------
# Public API — Children's book PDF assembly
# ---------------------------------------------------------------------------


def assemble_childrens_book_pdf(
    *,
    images: list[Path],
    scaffold: NarrativeScaffold,
    style_lock: StyleLock,
    title: str,
    author: str,
    output_dir: Path,
    page_texts: list[str] | None = None,
) -> Path:
    """Assemble a children's book PDF from text-free illustrations + scaffold.

    Args:
        images: Ordered list of text-free illustration image paths.
        scaffold: NarrativeScaffold with per-page structural data.
        style_lock: Locked visual parameters (palette, typography, strategy).
        title: Book title.
        author: Author name.
        output_dir: Directory to write generated .typ and .pdf files.
        page_texts: Story text per page. If None, uses scaffold
            ``checkpoint_progress`` as placeholder.

    Returns:
        Path to the generated PDF.
    """
    if page_texts is None:
        page_texts = [
            p.checkpoint_progress or f"Page {p.page}" for p in scaffold.pages
        ]

    typ_source = _generate_book_typst(
        images=images,
        page_texts=page_texts,
        scaffold=scaffold,
        style_lock=style_lock,
        title=title,
        author=author,
    )

    slug = title.lower().replace(" ", "_")[:30]
    typ_path = output_dir / f"{slug}.typ"
    pdf_path = output_dir / f"{slug}.pdf"

    typ_path.write_text(typ_source, encoding="utf-8")
    _compile_typst(typ_path, pdf_path)
    return pdf_path


# ---------------------------------------------------------------------------
# Public API — Ebook assembly (PDF + EPUB)
# ---------------------------------------------------------------------------


def assemble_ebook(
    *,
    sections: list[dict[str, str]],
    title: str,
    author: str,
    output_dir: Path,
    colors: dict[str, str] | None = None,
    fonts: dict[str, str] | None = None,
) -> dict[str, Path]:
    """Assemble an ebook as both PDF (Typst) and EPUB (ebooklib).

    Args:
        sections: List of dicts with ``title`` and ``content`` keys.
        title: Book title.
        author: Author name.
        output_dir: Directory for output files.
        colors: Optional ``primary``, ``secondary``, ``accent`` hex colors.
        fonts: Optional ``headline``, ``body`` font names.

    Returns:
        Dict with ``pdf`` and ``epub`` Path values.
    """
    colors = colors or {}
    fonts = fonts or {}

    # --- PDF via Typst (import ebook.typ template) ---
    ebook_template = (_TEMPLATES_DIR / "ebook.typ").resolve()
    chapter_lines: list[str] = []
    for section in sections:
        section_title = _escape_typst(section["title"])
        section_content = _escape_typst(section["content"])
        chapter_lines.append(f"= {section_title}")
        chapter_lines.append(section_content)
        chapter_lines.append("")

    typ_source = f'#include "{ebook_template}"\n\n' + "\n".join(chapter_lines)

    slug = title.lower().replace(" ", "_")[:30]
    typ_path = output_dir / f"{slug}.typ"
    pdf_path = output_dir / f"{slug}.pdf"
    epub_path = output_dir / f"{slug}.epub"

    typ_path.write_text(typ_source, encoding="utf-8")

    # Build --input flags for sys.inputs
    input_args: list[str] = [
        "--input", f"title={title}",
        "--input", f"author={author}",
    ]
    if "primary" in colors:
        input_args += ["--input", f"primary_color={colors['primary']}"]
    if "secondary" in colors:
        input_args += ["--input", f"secondary_color={colors['secondary']}"]
    if "accent" in colors:
        input_args += ["--input", f"accent_color={colors['accent']}"]
    if "headline" in fonts:
        input_args += ["--input", f"headline_font={fonts['headline']}"]
    if "body" in fonts:
        input_args += ["--input", f"body_font={fonts['body']}"]

    env = {**os.environ, "TYPST_FONT_PATHS": str(_FONTS_DIR)}
    result = subprocess.run(
        ["typst", "compile", "--root", "/", *input_args, str(typ_path), str(pdf_path)],
        capture_output=True,
        text=True,
        env=env,
        check=False,
    )
    if result.returncode != 0:
        logger.error("Ebook Typst compilation failed: %s", result.stderr)
        raise RuntimeError(f"Ebook Typst compilation failed: {result.stderr}")

    # --- EPUB via ebooklib ---
    book = epub.EpubBook()
    book.set_identifier(str(uuid4()))
    book.set_title(title)
    book.set_language("ms")
    book.add_author(author)

    style = epub.EpubItem(
        uid="style",
        file_name="style/default.css",
        media_type="text/css",
        content=_ebook_css().encode("utf-8"),
    )
    book.add_item(style)

    spine: list[str | epub.EpubHtml] = ["nav"]
    toc: list[epub.EpubHtml] = []

    for idx, section in enumerate(sections):
        chapter = epub.EpubHtml(
            title=section["title"],
            file_name=f"chapter_{idx + 1}.xhtml",
            lang="ms",
        )
        chapter.content = (
            f"<h1>{section['title']}</h1>"
            f"<p>{section['content']}</p>"
        ).encode("utf-8")
        chapter.add_item(style)
        book.add_item(chapter)
        spine.append(chapter)
        toc.append(chapter)

    book.toc = toc
    book.add_item(epub.EpubNcx())
    book.add_item(epub.EpubNav())
    book.spine = spine

    epub.write_epub(str(epub_path), book)
    return {"pdf": pdf_path, "epub": epub_path}


def _ebook_css() -> str:
    """Minimal CSS for ebook typography."""
    return (
        "body { font-family: serif; line-height: 1.6; margin: 1em; }\n"
        "h1 { font-size: 1.5em; margin-top: 2em; margin-bottom: 0.5em; }\n"
        "p { margin-bottom: 0.8em; text-indent: 1.5em; }\n"
    )


# ---------------------------------------------------------------------------
# Public API — Document PDF assembly (invoice, proposal, report, etc.)
# ---------------------------------------------------------------------------


def assemble_document_pdf(
    *,
    template_name: str,
    content: dict[str, str],
    colors: dict[str, str] | None = None,
    fonts: dict[str, str] | None = None,
    output_dir: Path,
) -> Path:
    """Assemble a business document PDF from an existing Typst template.

    Args:
        template_name: Name of the template (e.g. ``invoice``, ``proposal``).
        content: Key-value pairs passed as Typst sys.inputs.
        colors: Optional color overrides (primary, secondary, accent).
        fonts: Optional font overrides (headline, body).
        output_dir: Directory for the output PDF.

    Returns:
        Path to the generated PDF.
    """
    colors = colors or {}
    fonts = fonts or {}
    template_path = (_TEMPLATES_DIR / f"{template_name}.typ").resolve()
    if not template_path.exists():
        raise FileNotFoundError(f"Template not found: {template_path}")

    input_args: list[str] = []
    for key, value in content.items():
        input_args += ["--input", f"{key}={value}"]
    if "primary" in colors:
        input_args += ["--input", f"primary_color={colors['primary']}"]
    if "secondary" in colors:
        input_args += ["--input", f"secondary_color={colors['secondary']}"]
    if "accent" in colors:
        input_args += ["--input", f"accent_color={colors['accent']}"]
    if "headline" in fonts:
        input_args += ["--input", f"headline_font={fonts['headline']}"]
    if "body" in fonts:
        input_args += ["--input", f"body_font={fonts['body']}"]

    pdf_path = output_dir / f"{template_name}.pdf"
    env = {**os.environ, "TYPST_FONT_PATHS": str(_FONTS_DIR)}
    result = subprocess.run(
        ["typst", "compile", "--root", "/", *input_args, str(template_path), str(pdf_path)],
        capture_output=True,
        text=True,
        env=env,
        check=False,
    )
    if result.returncode != 0:
        logger.error("Document Typst compilation failed: %s", result.stderr)
        raise RuntimeError(f"Document Typst compilation failed: {result.stderr}")
    return pdf_path


