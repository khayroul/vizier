"""Publishing assembly pipeline (S15a).

Assembles text-free illustrations + text content + StyleLock config
into finished PDFs (via Typst) and EPUBs (via ebooklib).

Text is ALWAYS overlaid by Typst — NEVER rendered inside illustrations (#49).
Illustration prompts use ``illustration_shows`` field, not raw page text (§42.3).
"""

from __future__ import annotations

import logging
