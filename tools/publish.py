def update_rolling_context_for_page(
    ctx: RollingContext,
    page: PageScaffold,
    page_text: str,
    immutable_facts: list[str] | None = None,
) -> None:
    """Update RollingContext after producing a page.

    Adds the page content to the rolling summary, tracks characters
    as entities, and registers any immutable facts.

    Args:
        ctx: The RollingContext to update.
        page: The PageScaffold for this page.
        page_text: The generated text content for this page.
        immutable_facts: Optional list of facts to lock as immutable.
    """
    summary = (
        f"Page {page.page} ({page.emotional_beat}): {page_text}"
    )
    ctx.update(summary)

    # Track characters present on this page as entities
    for character_id in page.characters_present:
        entity = TrackedEntity(
            entity_id=character_id,
            entity_type="character",
            name=character_id,
            state={"last_emotional_beat": page.emotional_beat},
            introduced_at=ctx.current_step - 1,
            last_updated_at=ctx.current_step - 1,
        )
        ctx.add_entity(entity)

    # Register immutable facts
    if immutable_facts:
        for fact in immutable_facts:
            ctx.add_immutable_fact(fact, source=f"page_{page.page}")


